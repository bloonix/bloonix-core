sub form {
    my ($class, $type) = @_;
    my $codes = Bloonix::CCodes->$type();
    my @form;

    foreach my $key (sort keys %$codes) {
        push @form, {
            value => $key,
            name => $codes->{$key}
        };
    }

    return \@form;
}

